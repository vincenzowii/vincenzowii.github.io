<h2>Mail Script help:</h2>
<p><strong>How does this work?</strong></p>
<ul>
  <li>Using PHP's mail function to send the mail.</li>
  <li>See <a href="http://cr.yp.to/immhf.html" target="_blank">http://cr.yp.to/immhf.html</a> and <a href="http://www.faqs.org/rfcs/rfc822.html" target="_blank">RFC 822 </a></li>
</ul>
<h4>Sends field?</h4>
<ul>
  <li>This is the number of times that the message will be sent. </li>
  <li>You can specify 1-9 times, but we recommend only one. </li>
</ul>
<h4>What is CC and BCC?</h4>
<ul>
  <li>CC stands for Carbon copy, which is basically sending a copy more people. </li>
  <li>BCC stands for Blind Carbon copy, which is the same as a carbon copy, except that the other recipients can not see that it was sent to this address. </li>
</ul>
<h4>To/CC/BCC/From Formats:</h4>
<ul>
  <li>This   format is currently acceptable:
    <ul>
      <li>george@bush.com</li>
    </ul>
  </li>
</ul>
